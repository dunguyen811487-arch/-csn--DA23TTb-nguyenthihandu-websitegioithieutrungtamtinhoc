/* ===== RENDER REVIEW ===== */
function renderReview(review, index){
    const canDelete = review.owner === currentUser;
    const deleteButton = canDelete ? `
        <button class="btn btn-sm btn-outline-danger" onclick="deleteReview(${index})">
            <i class="fa-solid fa-trash"></i>
