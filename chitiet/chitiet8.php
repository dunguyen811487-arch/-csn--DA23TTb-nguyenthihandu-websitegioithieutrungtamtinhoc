<script>
const reviewForm = document.getElementById("reviewForm");
const reviewList = document.getElementById("reviewList");
const STORAGE_KEY = "reviews_tin_hoc_van_phong";

let currentUser = localStorage.getItem("currentUserReview") || "";

/* ===== LOAD REVIEW ===== */
